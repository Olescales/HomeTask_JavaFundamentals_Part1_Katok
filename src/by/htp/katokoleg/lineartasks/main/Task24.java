package by.htp.katokoleg.lineartasks.main;

import java.util.Scanner;

public class Task24 {

	// 24. Найти площадь равнобедренной трапеции с основаниями а и b и углом a при
	// большем основании а.

	public static void main(String[] args) {
		
		double a;
		double b;


		double katet;
		double height;
		double square;

		a = enterNumber("Введите большее основание трапеции: ");
		b = enterNumber("Введите меньшее основание трапеции: ");

		double angle = 44;
		
		while (angle < 45 || angle > 90) {

			angle = enterAngle("Enter correct angle (from 46 to 89 degrees): ");
			if (angle > 45 && angle < 90) {
				break;
			}
		}

		katet = (a - b) / 2;

		double angleInRadians = Math.tan(Math.toRadians(angle));

		height = katet * angleInRadians;

		System.out.println("Угол в радианах: " + angleInRadians);
		square = ((a + b) / 2) * height;
		System.out.println("Площадь трапеции: " + square);
	}

	public static double enterAngle(String message) {

		double angle;
		char b = '\u00B0'; // для значка символа 
	

		Scanner sc = new Scanner(System.in);
		System.out.print(message);

		while (!sc.hasNextDouble()) {
			String S;
			S = sc.next();
			System.out.println("Вы ввели некорректный угол: " + S + " " + b);
			System.out.println("Введите корректный угол: ");
		}
		angle = sc.nextDouble();
		return angle;
	}

	public static double enterNumber(String message) {

		double x;

		Scanner sc = new Scanner(System.in);
		System.out.print(message);
		while (!sc.hasNextDouble()) {
			String S;
			S = sc.next();
			System.out.println("Вы ввели неправильное число: " + S);
			System.out.println("Введите число: ");
		}
		x = sc.nextDouble();

		return x;
	}

}
